CREATE TABLE KEHOACH
(
    [Mã HDTD] NVARCHAR(50),
    [Kỳ] INT,
    [Số dư đầu kỳ] NUMERIC(18,0),
    [Ngày đầu kỳ] DATE,
    [Ngày cuối kỳ] DATE,
    [Số ngày tính lãi] INT,
    [Lãi suất trong hạn] NUMERIC(5,2),
    [Gốc phải trả] NUMERIC(18,0),
    [Lãi phải trả] NUMERIC(18,0),
    [Tổng tiền phải trả] NUMERIC(18,0)
);
GO

IF OBJECT_ID('KEHOACH_TRANO') IS NOT NULL
DROP PROC KEHOACH_TRANO
GO

CREATE PROC KEHOACH_TRANO
@MAHDTD NVARCHAR(20)
AS
BEGIN

-- Xóa kế hoạch cũ
DELETE FROM KEHOACH
WHERE [Mã HDTD] = @MAHDTD;

DECLARE
    @KYHAN INT,
    @LAISUAT NUMERIC(5,2),
    @SOTIEN NUMERIC(18,0),
    @NGAY_GN DATE,
    @NGAY_DH DATE,
    @I INT = 1,
    @GOCPHAITRA_MOIKY NUMERIC(18,0);

-- Lấy dữ liệu hợp đồng
SELECT
    @KYHAN = KY_HAN,
    @LAISUAT = LAISUAT,
    @SOTIEN = SOTIEN,
    @NGAY_GN = NGAY_GIAINGAN,
    @NGAY_DH = NGAY_DAOHAN
FROM HOPDONG_TINDUNG
WHERE MA_HOPDONG_TINDUNG = @MAHDTD;

-- Gốc mỗi kỳ
SET @GOCPHAITRA_MOIKY = ROUND(@SOTIEN*1.0/@KYHAN,0);

-- Tạo kế hoạch trả nợ
WHILE @I <= @KYHAN
BEGIN

INSERT INTO KEHOACH
VALUES
(
 @MAHDTD,

 @I,

 @SOTIEN-(@I-1)*@GOCPHAITRA_MOIKY,

 @NGAY_GN,

 DATEADD(DAY,-1,DATEADD(MONTH,1,@NGAY_GN)),

 0,

 @LAISUAT,

 @GOCPHAITRA_MOIKY,

 0,

 0
);

SET @I=@I+1;
SET @NGAY_GN=DATEADD(MONTH,1,@NGAY_GN);

END
-- Điều chỉnh kỳ cuối
UPDATE KEHOACH
SET
 [Ngày cuối kỳ] = @NGAY_DH,

 [Gốc phải trả] =
 @SOTIEN -
 (
   SELECT SUM([Gốc phải trả])
   FROM KEHOACH
   WHERE [Mã HDTD]=@MAHDTD
   AND [Kỳ]<@KYHAN
 )

WHERE [Mã HDTD]=@MAHDTD
AND [Kỳ]=@KYHAN;

-- Tính số ngày
UPDATE KEHOACH
SET [Số ngày tính lãi] =
DATEDIFF(DAY,[Ngày đầu kỳ],[Ngày cuối kỳ])+1
WHERE [Mã HDTD]=@MAHDTD;

-- Tính lãi
UPDATE KEHOACH
SET [Lãi phải trả] =
ROUND([Số ngày tính lãi]*[Lãi suất trong hạn]*[Số dư đầu kỳ]/36500,0)
WHERE [Mã HDTD]=@MAHDTD;

-- Tổng tiền
UPDATE KEHOACH
SET [Tổng tiền phải trả] =
[Gốc phải trả]+[Lãi phải trả]
WHERE [Mã HDTD]=@MAHDTD;

END
GO

EXEC KEHOACH_TRANO 'CRCT-00186';

SELECT *
FROM KEHOACH
ORDER BY [Mã HDTD],[Kỳ];
