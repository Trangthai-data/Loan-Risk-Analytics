CREATE VIEW v_BAOCAO_TINDUNG
AS

SELECT      
        HTD.MA_HOPDONG_TINDUNG,
        HTD.SOTIEN,
        MD.TEN_MUCDICH_CAP1,
        HT.TEN_HINHTHUC_CAP1,
        NN1.TEN_NGANHNGHE_KINHTE_CAP01,
        SP.TENSP,
        HTD.NGAY_GIAINGAN

FROM        HOPDONG_TINDUNG HTD

INNER JOIN  MUCDICH_CAPTINDUNG MD
ON HTD.MA_MUCDICH_CAPTD = MD.MA_MUCDICH_CAPTD

INNER JOIN  HINHTHUC_CAPTINDUNG HT
ON HTD.MA_HINHTHUC_CAPTD = HT.MA_HINHTHUC_CAPTD

INNER JOIN  KHACHHANG KH
ON HTD.MA_KHACHHANG = KH.MA_KHACHHANG

INNER JOIN  NGANHNGHE_KINHTE_CAP03 NN3
ON KH.MA_NGANHNGHE_KINHTE = NN3.MA_NGANHNGHE_KINHTE_CAP03

INNER JOIN  NGANHNGHE_KINHTE_CAP01 NN1
ON NN3.MA_NGANHNGHE_KINHTE_CAP01 = NN1.MA_NGANHNGHE_KINHTE_CAP01

INNER JOIN SANPHAM_TINDUNG SP
ON HTD.MA_SANPHAM_TD = SP.MASP;

SELECT *
FROM v_BAOCAO_TINDUNG
WHERE YEAR(NGAY_GIAINGAN) = 2024;

