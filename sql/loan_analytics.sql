-- Procedure: SP_LOAN_AMORTIZATION_SCHEDULE
-- Purpose:
-- Generate loan repayment schedules and amortization
-- cashflow projections for lending analytics
--
-- Features:
-- - Outstanding balance tracking
-- - Principal repayment calculation
-- - Interest accrual calculation
-- - Installment cashflow generation
--
-- Author: Trang Thai
-- =========================================================

CREATE PROC PRC_TRANGTH_BAOCAO_TINDUNG_2024
    @year INT -- Năm cần lọc
AS
BEGIN
    -- Bảng lưu kết quả báo cáo
    DECLARE @BAOCAO TABLE (
        TIEUCHI NVARCHAR(255),           -- Tiêu chí
        SOLUONG_HDTD INT,                -- Số lượng hợp đồng tín dụng
        TILE_SOLUONG FLOAT,              -- Tỷ lệ số lượng (%)
        TONGTIEN_GIAINGAN NUMERIC(18,2), -- Tổng số tiền giải ngân
        TILE_TONGTIEN FLOAT              -- Tỷ lệ tổng số tiền (%)
    );

    -- Tổng số lượng hợp đồng và tổng tiền giải ngân theo năm
    DECLARE @TOTAL_HDTD INT, @TOTAL_TIEN NUMERIC(18,2);
    SELECT 
        @TOTAL_HDTD = ISNULL(COUNT(*), 0), 
        @TOTAL_TIEN = ISNULL(SUM(SOTIEN), 0)
    FROM HOPDONG_TINDUNG
    WHERE YEAR(NGAY_GIAINGAN) = @year;

    -- I. Tổng hợp theo mục đích cấp tín dụng
    INSERT INTO @BAOCAO
    SELECT 
        N'I. Mục đích cấp tín dụng' AS TIEUCHI,
        @TOTAL_HDTD AS SOLUONG_HDTD,
        100 AS TILE_SOLUONG,
        @TOTAL_TIEN AS TONGTIEN_GIAINGAN,
        100 AS TILE_TONGTIEN;

    -- I.1 Tín dụng bất động sản
    INSERT INTO @BAOCAO
    SELECT 
        N'I.1 Tín dụng bất động sản',
        ISNULL(COUNT(*), 0) AS SOLUONG_HDTD,
        ROUND(CAST(ISNULL(COUNT(*), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT),2) AS TILE_SOLUONG,
        ISNULL(SUM(SOTIEN), 0) AS TONGTIEN_GIAINGAN,
        ROUND(CAST(ISNULL(SUM(SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT),2) AS TILE_TONGTIEN
    FROM HOPDONG_TINDUNG HTD
    INNER JOIN MUCDICH_CAPTINDUNG MD ON HTD.MA_MUCDICH_CAPTD = MD.MA_MUCDICH_CAPTD
    WHERE YEAR(HTD.NGAY_GIAINGAN) = @year AND MD.MA_CAP1 = 1;

    -- I.2 Tín dụng phục vụ nhu cầu đời sống
    INSERT INTO @BAOCAO
    SELECT 
        N'I.2 Tín dụng phục vụ nhu cầu đời sống',
        ISNULL(COUNT(*), 0) AS SOLUONG_HDTD,
        ROUND(CAST(ISNULL(COUNT(*), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT),2) AS TILE_SOLUONG,
        ISNULL(SUM(SOTIEN), 0) AS TONGTIEN_GIAINGAN,
        ROUND(CAST(ISNULL(SUM(SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT),2) AS TILE_TONGTIEN
    FROM HOPDONG_TINDUNG HTD
    INNER JOIN MUCDICH_CAPTINDUNG MD ON HTD.MA_MUCDICH_CAPTD = MD.MA_MUCDICH_CAPTD
    WHERE YEAR(HTD.NGAY_GIAINGAN) = @year AND MD.MA_CAP1 = 2;

    -- I.3 Tín dụng đầu tư
    INSERT INTO @BAOCAO
    SELECT 
        N'I.3 Tín dụng đầu tư',
        ISNULL(COUNT(*), 0) AS SOLUONG_HDTD,
        ROUND(CAST(ISNULL(COUNT(*), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT),2) AS TILE_SOLUONG,
        ISNULL(SUM(SOTIEN), 0) AS TONGTIEN_GIAINGAN,
        ROUND(CAST(ISNULL(SUM(SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT),2) AS TILE_TONGTIEN
    FROM HOPDONG_TINDUNG HTD
    INNER JOIN MUCDICH_CAPTINDUNG MD ON HTD.MA_MUCDICH_CAPTD = MD.MA_MUCDICH_CAPTD
    WHERE YEAR(HTD.NGAY_GIAINGAN) = @year AND MD.MA_CAP1 = 3;

    -- II. Tổng hợp theo hình thức cấp tín dụng
    INSERT INTO @BAOCAO
    SELECT 
        N'II. Hình thức cấp tín dụng' AS TIEUCHI,
        @TOTAL_HDTD AS SOLUONG_HDTD,
        100 AS TILE_SOLUONG,
        @TOTAL_TIEN AS TONGTIEN_GIAINGAN,
        100 AS TILE_TONGTIEN;

    -- II.1 Cho vay
    INSERT INTO @BAOCAO
    SELECT 
        N'II.1 Cho vay',
        ISNULL(COUNT(*), 0) AS SOLUONG_HDTD,
        ROUND(CAST(ISNULL(COUNT(*), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT),2) AS TILE_SOLUONG,
        ISNULL(SUM(SOTIEN), 0) AS TONGTIEN_GIAINGAN,
        ROUND(CAST(ISNULL(SUM(SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT),2) AS TILE_TONGTIEN
    FROM HOPDONG_TINDUNG HTD
    INNER JOIN HINHTHUC_CAPTINDUNG HT ON HTD.MA_HINHTHUC_CAPTD = HT.MA_HINHTHUC_CAPTD
    WHERE YEAR(HTD.NGAY_GIAINGAN) = @year AND HT.MA_CAP1 = 1;

    -- II.2 Cho thuê tài chính
    INSERT INTO @BAOCAO
    SELECT 
        N'II.2 Cho thuê tài chính',
        ISNULL(COUNT(*), 0) AS SOLUONG_HDTD,
        ROUND(CAST(ISNULL(COUNT(*), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT),2) AS TILE_SOLUONG,
        ISNULL(SUM(SOTIEN), 0) AS TONGTIEN_GIAINGAN,
        ROUND(CAST(ISNULL(SUM(SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT),2) AS TILE_TONGTIEN
    FROM HOPDONG_TINDUNG HTD
    INNER JOIN HINHTHUC_CAPTINDUNG HT ON HTD.MA_HINHTHUC_CAPTD = HT.MA_HINHTHUC_CAPTD
    WHERE YEAR(HTD.NGAY_GIAINGAN) = @year AND HT.MA_CAP1 = 2;

    -- II.3 Chiết khấu
    INSERT INTO @BAOCAO
    SELECT 
        N'II.3 Chiết khấu',
        ISNULL(COUNT(*), 0) AS SOLUONG_HDTD,
        ROUND(CAST(ISNULL(COUNT(*), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT),2) AS TILE_SOLUONG,
        ISNULL(SUM(SOTIEN), 0) AS TONGTIEN_GIAINGAN,
        ROUND(CAST(ISNULL(SUM(SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT),2) AS TILE_TONGTIEN
    FROM HOPDONG_TINDUNG HTD
    INNER JOIN HINHTHUC_CAPTINDUNG HT ON HTD.MA_HINHTHUC_CAPTD = HT.MA_HINHTHUC_CAPTD
    WHERE YEAR(HTD.NGAY_GIAINGAN) = @year AND HT.MA_CAP1 = 3;

    -- II.4 Bao thanh toán
    INSERT INTO @BAOCAO
    SELECT 
        N'II.4 Bao thanh toán',
        ISNULL(COUNT(*), 0) AS SOLUONG_HDTD,
        ROUND(CAST(ISNULL(COUNT(*), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT),2) AS TILE_SOLUONG,
        ISNULL(SUM(SOTIEN), 0) AS TONGTIEN_GIAINGAN,
        ROUND(CAST(ISNULL(SUM(SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT),2) AS TILE_TONGTIEN
    FROM HOPDONG_TINDUNG HTD
    INNER JOIN HINHTHUC_CAPTINDUNG HT ON HTD.MA_HINHTHUC_CAPTD = HT.MA_HINHTHUC_CAPTD
    WHERE YEAR(HTD.NGAY_GIAINGAN) = @year AND HT.MA_CAP1 = 4;

    -- II.5 Trả thay theo cam kết ngoại bảng
    INSERT INTO @BAOCAO
    SELECT 
        N'II.5 Trả thay theo cam kết ngoại bảng',
        ISNULL(COUNT(*), 0) AS SOLUONG_HDTD,
        ROUND(CAST(ISNULL(COUNT(*), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT),2) AS TILE_SOLUONG,
        ISNULL(SUM(SOTIEN), 0) AS TONGTIEN_GIAINGAN,
        ROUND(CAST(ISNULL(SUM(SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT),2) AS TILE_TONGTIEN
    FROM HOPDONG_TINDUNG HTD
    INNER JOIN HINHTHUC_CAPTINDUNG HT ON HTD.MA_HINHTHUC_CAPTD = HT.MA_HINHTHUC_CAPTD
    WHERE YEAR(HTD.NGAY_GIAINGAN) = @year AND HT.MA_CAP1 = 5;

    -- II.6 Ủy thác
    INSERT INTO @BAOCAO
    SELECT 
        N'II.6 Ủy thác',
        ISNULL(COUNT(*), 0) AS SOLUONG_HDTD,
        ROUND(CAST(ISNULL(COUNT(*), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT),2) AS TILE_SOLUONG,
        ISNULL(SUM(SOTIEN), 0) AS TONGTIEN_GIAINGAN,
        ROUND(CAST(ISNULL(SUM(SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT),2) AS TILE_TONGTIEN
    FROM HOPDONG_TINDUNG HTD
    INNER JOIN HINHTHUC_CAPTINDUNG HT ON HTD.MA_HINHTHUC_CAPTD = HT.MA_HINHTHUC_CAPTD
    WHERE YEAR(HTD.NGAY_GIAINGAN) = @year AND HT.MA_CAP1 = 6;

	-- III. Tổng hợp toàn bộ ngành nghề kinh tế
	INSERT INTO @BAOCAO
	SELECT 
		N'III. Theo ngành nghề kinh tế' AS TIEUCHI,
		@TOTAL_HDTD AS SOLUONG_HDTD,
		100 AS TILE_SOLUONG,
		@TOTAL_TIEN AS TONGTIEN_GIAINGAN,
		100 AS TILE_TONGTIEN;

	-- Liên kết và tổng hợp theo từng ngành nghề
	INSERT INTO @BAOCAO
	SELECT 
		CONCAT(N'III.', NN1.MA_NGANHNGHE_KINHTE_CAP01, ' ', NN1.TEN_NGANHNGHE_KINHTE_CAP01) AS TIEUCHI,
		ISNULL(COUNT(HTD.MA_HOPDONG_TINDUNG), 0) AS SOLUONG_HDTD,
		ROUND(CAST(ISNULL(COUNT(HTD.MA_HOPDONG_TINDUNG), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT), 2) AS TILE_SOLUONG,
		ISNULL(SUM(HTD.SOTIEN), 0) AS TONGTIEN_GIAINGAN,
		ROUND(CAST(ISNULL(SUM(HTD.SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT), 2) AS TILE_TONGTIEN
	FROM NGANHNGHE_KINHTE_CAP01 NN1
	LEFT JOIN NGANHNGHE_KINHTE_CAP02 NN2 ON NN1.MA_NGANHNGHE_KINHTE_CAP01 = NN2.MA_NGANHNGHE_KINHTE_CAP01
	LEFT JOIN NGANHNGHE_KINHTE_CAP03 NN3 ON NN2.MA_NGANHNGHE_KINHTE_CAP02 = NN3.MA_NGANHNGHE_KINHTE_CAP02
	LEFT JOIN KHACHHANG KH ON NN3.MA_NGANHNGHE_KINHTE_CAP03 = KH.MA_NGANHNGHE_KINHTE
	LEFT JOIN HOPDONG_TINDUNG HTD ON KH.MA_KHACHHANG = HTD.MA_KHACHHANG AND YEAR(HTD.NGAY_GIAINGAN) = @year
	GROUP BY NN1.MA_NGANHNGHE_KINHTE_CAP01, NN1.TEN_NGANHNGHE_KINHTE_CAP01;

    -- IV. Tổng hợp toàn bộ sản phẩm tín dụng
    INSERT INTO @BAOCAO
    SELECT 
        N'IV. Theo sản phẩm tín dụng' AS TIEUCHI,
        @TOTAL_HDTD AS SOLUONG_HDTD,
        100 AS TILE_SOLUONG,
        @TOTAL_TIEN AS TONGTIEN_GIAINGAN,
        100 AS TILE_TONGTIEN;

    -- Báo cáo theo từng sản phẩm tín dụng
    INSERT INTO @BAOCAO
    SELECT 
        CONCAT(N'IV.', SP.MASP, ' ', SP.TENSP) AS TIEUCHI,
        ISNULL(COUNT(*), 0) AS SOLUONG_HDTD,
        ROUND(CAST(ISNULL(COUNT(*), 0) * 100.0 / ISNULL(@TOTAL_HDTD, 1) AS FLOAT), 2) AS TILE_SOLUONG,
        ISNULL(SUM(HTD.SOTIEN), 0) AS TONGTIEN_GIAINGAN,
        ROUND(CAST(ISNULL(SUM(HTD.SOTIEN), 0) * 100.0 / ISNULL(@TOTAL_TIEN, 1) AS FLOAT), 2) AS TILE_TONGTIEN
    FROM HOPDONG_TINDUNG HTD
    INNER JOIN SANPHAM_TINDUNG SP ON HTD.MA_SANPHAM_TD = SP.MASP
    WHERE YEAR(HTD.NGAY_GIAINGAN) = @year
    GROUP BY SP.MASP, SP.TENSP;

    -- Trả kết quả báo cáo
    SELECT * FROM @BAOCAO;
END;

--- NHẬP năm (@year) ĐƯA RA KẾT QUẢ

EXEC PRC_TRANGTH_BAOCAO_TINDUNG_2024 @year = 2024;


